export class Curso {
        constructor(id, url, author, name, score, stars, votes, price, pricePrevous, portada, matriculados, lastUpdate, language, description) {
                this.id = id;
                this.url = url;
                this.author = author;
                this.name = name;
                this.score = score;
                this.stars = stars;
                this.votes = votes;
                this.price = price;
                this.pricePrevous = pricePrevous;
                this.portada = portada;
                this.matriculados = matriculados;
                this.lastUpdate = lastUpdate;
                this.language = language;
                this.description = description;
        }
}